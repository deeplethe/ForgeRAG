# ---------------------------------------------------------------------------
# Main wizard
# ---------------------------------------------------------------------------


def run_wizard(quick: bool = False):
    banner("ForgeRAG Docker Setup")
    info("This wizard will configure Docker Compose for ForgeRAG.")
    info("It generates docker/config.yaml and .env, then optionally starts the stack.")

    opts: dict = {}

    # ── LLM Provider ──
    section("LLM Provider")
    if quick:
        provider = "openai"
    else:
        provider = ask_choice(
            "Which LLM provider?",
            [
                ("openai", "OpenAI (GPT-4o-mini, text-embedding-3-small)"),
                ("deepseek", "DeepSeek (deepseek-chat, deepseek-embedding)"),
                ("ollama", "Ollama local (requires Ollama running on host)"),
                ("custom", "Custom (enter model strings manually)"),
            ],
            default="openai",
        )

    if provider != "custom":
        preset = _PROVIDER_PRESETS[provider]
        opts.update(preset)
        info(f"  Chat model:  {_c(opts['chat_model'], 'bold')}")
        info(f"  Embed model: {_c(opts['embed_model'], 'bold')}")
        info(f"  Dimension:   {_c(str(opts['dimension']), 'bold')}")
    else:
        opts["api_key_env"] = ask("API key env var name", "OPENAI_API_KEY")
        opts["chat_model"] = ask("Chat model (litellm format)", "openai/gpt-4o-mini")
        opts["embed_model"] = ask("Embedding model", "openai/text-embedding-3-small")
        opts["dimension"] = int(ask("Embedding dimension", "1536"))
        opts["api_base"] = ask("API base URL (leave empty for default)", "") or None

    # API key value
    section("API Key")
    if provider == "ollama":
        opts["api_key_value"] = "ollama"
        info("Ollama doesn't need an API key, using placeholder.")
    elif quick:
        existing = os.environ.get(opts["api_key_env"], "")
        if existing:
            opts["api_key_value"] = existing
            success(f"Found {opts['api_key_env']} in environment.")
        else:
            opts["api_key_value"] = ask(f"Enter your {opts['api_key_env']}")
    else:
        existing = os.environ.get(opts["api_key_env"], "")
        if existing:
            use_existing = ask_bool(f"Found {opts['api_key_env']} in environment. Use it?", True)
            if use_existing:
                opts["api_key_value"] = existing
            else:
                opts["api_key_value"] = ask(f"Enter your {opts['api_key_env']}")
        else:
            opts["api_key_value"] = ask(f"Enter your {opts['api_key_env']}")

    # ── Passwords ──
    section("Database Passwords")
    if quick:
        opts["pg_password"] = secrets.token_urlsafe(16)
        info("Generated Postgres password.")
    else:
        opts["pg_password"] = ask("Postgres password", secrets.token_urlsafe(16))

    # ── Neo4j ──
    section("Knowledge Graph")
    if quick:
        opts["neo4j"] = False
    else:
        opts["neo4j"] = ask_bool("Enable Neo4j for knowledge graph? (adds a Neo4j container)", False)

    if opts["neo4j"]:
        if quick:
            opts["neo4j_password"] = secrets.token_urlsafe(16)
        else:
            opts["neo4j_password"] = ask("Neo4j password", secrets.token_urlsafe(16))
        opts["kg_extraction"] = ask_bool("Enable KG extraction during ingestion?", True)
    else:
        opts["kg_extraction"] = False

    # ── Write files ──
    section("Writing configuration")

    _DOCKER_DIR.mkdir(exist_ok=True)
    config_path = _DOCKER_DIR / "config.yaml"
    config_content = generate_config(opts)
    config_path.write_text(config_content, encoding="utf-8")
    success("docker/config.yaml")

    env_content = generate_env(opts)
    _ENV_FILE.write_text(env_content, encoding="utf-8")
    success(".env")

    # ── Summary ──
    banner("Ready to launch")

    compose_cmd = "docker compose up -d"
    if opts.get("neo4j"):
        compose_cmd = "docker compose --profile neo4j up -d"

    info(f"Provider:  {_c(provider, 'bold')}")
    info(f"Chat:      {_c(opts['chat_model'], 'bold')}")
    info(f"Embed:     {_c(opts['embed_model'], 'bold')}")
    info(f"Neo4j:     {_c('yes' if opts.get('neo4j') else 'no', 'bold')}")
    print()
    info("To start ForgeRAG:")
    print()
    print(f"    {_c(compose_cmd, 'green')}")
    print()
    info(f"Then open {_c('http://localhost:8000', 'cyan')}")
    print()

    if not quick:
        if ask_bool("Start now?", True):
            print()
            info("Starting Docker Compose...")
            print()
            subprocess.run(compose_cmd.split(), cwd=str(_ROOT))


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------


def main():
    parser = argparse.ArgumentParser(description="ForgeRAG Docker setup wizard")
    parser.add_argument("--quick", action="store_true", help="Accept all defaults (OpenAI, no Neo4j)")
    args = parser.parse_args()

    try:
        run_wizard(quick=args.quick)
    except Aborted:
        print()
        print(_c("  Aborted.", "dim"))
        sys.exit(1)


if __name__ == "__main__":
    main()
