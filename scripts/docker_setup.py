"""
Docker deployment wizard for ForgeRAG.

Guides the user through Docker Compose configuration, generates
docker/config.yaml and .env, and optionally starts the stack.

Usage:
    python scripts/docker_setup.py              # interactive wizard
    python scripts/docker_setup.py --quick      # accept all defaults
"""

from __future__ import annotations

import argparse
import os
import secrets
import subprocess
import sys
from pathlib import Path

_ROOT = Path(__file__).resolve().parent.parent
_DOCKER_DIR = _ROOT / "docker"
_ENV_FILE = _ROOT / ".env"


# ---------------------------------------------------------------------------
# Pretty printing
# ---------------------------------------------------------------------------


def _is_tty() -> bool:
    return sys.stdout.isatty()


def _c(text: str, color: str) -> str:
    if not _is_tty():
        return str(text)
    codes = {
        "bold": "1",
        "dim": "2",
        "green": "32",
        "yellow": "33",
        "blue": "34",
        "cyan": "36",
        "red": "31",
    }
    return f"\033[{codes.get(color, '0')}m{text}\033[0m"


def banner(title: str):
    print()
    print(_c("━" * 60, "dim"))
    print(_c(f" {title}", "bold"))
    print(_c("━" * 60, "dim"))


def section(title: str):
    print()
    print(_c(f"▸ {title}", "cyan"))


def info(msg: str):
    print(f"  {msg}")


def success(msg: str):
    print(_c(f"  ✓ {msg}", "green"))


def warn(msg: str):
    print(_c(f"  ⚠ {msg}", "yellow"))


# ---------------------------------------------------------------------------
# Prompt helpers
# ---------------------------------------------------------------------------


class Aborted(Exception):
    pass


def ask(question: str, default: str | None = None) -> str:
    suffix = f" [{_c(default, 'yellow')}]" if default else ""
    while True:
        try:
            raw = input(f"  {question}{suffix}: ").strip()
        except (EOFError, KeyboardInterrupt):
            raise Aborted()
        val = raw or default
        if val:
            return val
        print(_c("  (required)", "red"))


def ask_bool(question: str, default: bool = False) -> bool:
    tip = "Y/n" if default else "y/N"
    while True:
        try:
            raw = input(f"  {question} [{_c(tip, 'yellow')}]: ").strip().lower()
        except (EOFError, KeyboardInterrupt):
            raise Aborted()
        if not raw:
            return default
        if raw in ("y", "yes"):
            return True
        if raw in ("n", "no"):
            return False


def ask_choice(question: str, options: list[tuple[str, str]], default: str | None = None) -> str:
    print(f"  {question}")
    default_idx = None
    for i, (val, desc) in enumerate(options, 1):
        marker = _c(" (default)", "dim") if val == default else ""
        print(f"    {i}) {_c(val, 'bold')}  {_c('— ' + desc, 'dim')}{marker}")
        if val == default:
            default_idx = i
    while True:
        try:
            default_hint = " [" + _c(str(default_idx), "yellow") + "]" if default_idx else ""
            raw = input(f"  enter [1-{len(options)}]{default_hint}: ").strip()
        except (EOFError, KeyboardInterrupt):
            raise Aborted()
        if not raw and default_idx:
            return options[default_idx - 1][0]
        if raw.isdigit() and 1 <= int(raw) <= len(options):
            return options[int(raw) - 1][0]


# ---------------------------------------------------------------------------
# Config generation
# ---------------------------------------------------------------------------


def generate_config(opts: dict) -> str:
    """Generate docker/config.yaml content."""
    lines = [
        "# Generated by docker_setup.py",
        "# Postgres + pgvector deployment",
        "",
        "persistence:",
        "  relational:",
        "    backend: postgres",
        "    postgres:",
        "      host: postgres",
        "      port: 5432",
        "      database: opencraig",
        "      user: opencraig",
        "      password_env: POSTGRES_PASSWORD",
        "  vector:",
        "    backend: pgvector",
        "    pgvector:",
        f"      dimension: {opts['dimension']}",
        "      distance: cosine",
        "      index_type: hnsw",
        "",
        "storage:",
        "  mode: local",
        "  local:",
        "    root: /app/storage/blobs",
        "",
        "embedder:",
        "  backend: litellm",
        f"  dimension: {opts['dimension']}",
        "  litellm:",
        f"    model: {opts['embed_model']}",
        f"    api_key_env: {opts['api_key_env']}",
    ]

    if opts.get("api_base"):
        lines.append(f"    api_base: {opts['api_base']}")

    lines += [
        "",
        "answering:",
        "  generator:",
        "    backend: litellm",
        f"    model: {opts['chat_model']}",
        f"    api_key_env: {opts['api_key_env']}",
    ]

    if opts.get("api_base"):
        lines.append(f"    api_base: {opts['api_base']}")

    if opts.get("neo4j"):
        lines += [
            "",
            "graph:",
            "  backend: neo4j",
            "  neo4j:",
            "    uri: bolt://neo4j:7687",
            "    user: neo4j",
            "    password_env: NEO4J_PASSWORD",
        ]

    if opts.get("kg_extraction"):
        lines += [
            "",
            "retrieval:",
            "  kg_extraction:",
            "    enabled: true",
            f"    model: {opts['chat_model']}",
            f"    api_key_env: {opts['api_key_env']}",
            "  kg_path:",
            "    enabled: true",
            f"    model: {opts['chat_model']}",
            f"    api_key_env: {opts['api_key_env']}",
        ]

    lines.append("")
    return "\n".join(lines)


def generate_env(opts: dict) -> str:
    """Generate .env content."""
    lines = [
        "# Generated by docker_setup.py",
        f"{opts['api_key_env']}={opts['api_key_value']}",
        f"POSTGRES_PASSWORD={opts['pg_password']}",
    ]
    if opts.get("neo4j"):
        lines.append(f"NEO4J_PASSWORD={opts['neo4j_password']}")
    lines.append("")
    return "\n".join(lines)


